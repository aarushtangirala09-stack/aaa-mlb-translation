# FIGURE 09 — NEW: before/after specification correction
# =============================================================================
def figure_09() -> None:
    # Numbers documented in NOTES.md — uncorrected vs corrected pipelines
    labels = ["AAA ISO", "AAA K%", "AAA BB%", "AAA BABIP", "Age"]
    uncorrected = [+6.32, -2.96, +2.86, +0.55, -6.25]
    corrected   = [+5.48, -3.54, +2.64, +0.43, -1.92]

    fig, ax = plt.subplots(figsize=(11.5, 6.5))
    xs = np.arange(len(labels))
    w = 0.35
    b1 = ax.bar(xs - w/2, uncorrected, w, label="Uncorrected pipeline (N = 624)",
                color="#D3A5A5", edgecolor="black", linewidth=0.8, alpha=0.9)
    b2 = ax.bar(xs + w/2, corrected, w, label="Corrected pipeline (N = 486)",
                color="#5A7CA6", edgecolor="black", linewidth=0.8, alpha=0.9)

    for i, (u, c) in enumerate(zip(uncorrected, corrected)):
        ax.text(i - w/2, u + (0.25 if u > 0 else -0.25), f"{u:+.2f}",
                ha="center", va="bottom" if u > 0 else "top",
                fontsize=10.5, fontweight="bold", color="#7B3F3F")
        ax.text(i + w/2, c + (0.25 if c > 0 else -0.25), f"{c:+.2f}",
                ha="center", va="bottom" if c > 0 else "top",
                fontsize=10.5, fontweight="bold", color="#2C4A6E")

    # highlight the age collapse specifically
    ax.annotate(
        "Age coefficient collapsed\nfrom −6.25 to −1.92 (~70%)\nafter removing temporal leakage",
        xy=(4 + w/2, -1.92), xycoords="data",
        xytext=(3.3, -4.7), textcoords="data",
        fontsize=11.5, ha="center", color="#333",
        bbox=dict(boxstyle="round,pad=0.4", facecolor="#FFF3B0",
                  edgecolor="#B8860B", linewidth=1),
        arrowprops=dict(arrowstyle="->", color="#B8860B", lw=1.5,
                        connectionstyle="arc3,rad=-0.15"),
    )

    ax.set_xticks(xs)
    ax.set_xticklabels(labels)
    ax.axhline(0, color="black", lw=0.8)
    ax.set_ylabel("Standardized coefficient on MLB wRC+")
    ax.legend(loc="upper right", framealpha=0.9)
    ax.set_ylim(-7.5, 8.5)
    add_title_and_subtitle(
        fig,
        "Skill-model coefficients: uncorrected vs. corrected pipeline",
        "Uncorrected pipeline used career-PA-weighted AAA aggregates and \"age in final AAA season.\"  "
        "Corrected pipeline restricts to pre-debut AAA seasons and uses age at MLB debut.",
    )
    fig.subplots_adjust(top=0.83)
    fig.savefig(FIGS / "09_before_after_correction.png")
    plt.close(fig)
    print("wrote figures/09_before_after_correction.png")


