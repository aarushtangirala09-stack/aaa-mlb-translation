# FIGURE 03 — THE HEADLINE FIGURE (skill coefficients)
# =============================================================================
def figure_03() -> None:
    coefs = pd.read_csv(COEFS)
    skill = coefs[coefs["model"] == "skill"].copy()

    labels_map = {
        "aaa_ISO_zseason":   "AAA ISO\n(power)",
        "aaa_K%_zseason":    "AAA K%\n(strikeouts)",
        "aaa_BB%_zseason":   "AAA BB%\n(walks)",
        "age_at_debut":      "Age at\nMLB debut",
        "aaa_BABIP_zseason": "AAA BABIP\n(luck)",
    }
    skill["label"] = skill["predictor"].map(labels_map)
    skill = skill.dropna(subset=["label"])
    skill = skill.reindex(skill["std_coef"].abs().sort_values().index)

    colors = []
    for _, row in skill.iterrows():
        if row["p_value"] > 0.05:
            colors.append(COLOR_NULL)
        elif row["std_coef"] > 0:
            colors.append(COLOR_POS)
        else:
            colors.append(COLOR_NEG)

    fig, ax = plt.subplots(figsize=(11, 6.5))
    bars = ax.barh(skill["label"], skill["std_coef"],
                   xerr=1.96 * skill["std_error"],
                   color=colors, edgecolor="black", linewidth=0.9, alpha=0.9,
                   error_kw={"lw": 1.5, "capsize": 4})
    ax.axvline(0, color="black", lw=1)

    # value labels
    for bar, val, p in zip(bars, skill["std_coef"], skill["p_value"]):
        if p < 0.001:
            stars = "***"
        elif p < 0.01:
            stars = "**"
        elif p < 0.05:
            stars = "*"
        else:
            stars = "n.s."
        offset = 0.35 if val > 0 else -0.35
        ha = "left" if val > 0 else "right"
        ax.text(val + offset, bar.get_y() + bar.get_height()/2,
                f"{val:+.2f} {stars}", va="center", ha=ha,
                fontweight="bold", fontsize=12.5)

    ax.set_xlabel("Standardized coefficient   ← lowers MLB wRC+   |   raises MLB wRC+ →",
                  labelpad=10)
    ax.text(0.5, -0.19,
            "***  p < 0.001    **  p < 0.01    *  p < 0.05    n.s.  not significant. "
            "Bars = predicted change in MLB wRC+ per 1 standard-deviation change in the AAA input.",
            transform=ax.transAxes, ha="center", va="top", fontsize=10, color="#555")

    lo = min(skill["std_coef"].min() * 1.15, -1)
    hi = max(skill["std_coef"].max() * 1.15, 1)
    span = max(abs(lo), abs(hi))
    ax.set_xlim(-span * 1.1, span * 1.1)
    add_title_and_subtitle(
        fig,
        "Standardized skill-model regression coefficients",
        "Bars show predicted change in MLB wRC+ per 1-SD change in each AAA predictor. "
        "Green = positive translator, red = negative, gray = null.",
    )
    fig.subplots_adjust(top=0.83, bottom=0.18)
    fig.savefig(FIGS / "03_skill_coefficients.png")
    plt.close(fig)
    print("wrote figures/03_skill_coefficients.png")


