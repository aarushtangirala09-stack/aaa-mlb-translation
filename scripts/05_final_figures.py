# FIGURE 08 — Coefficient robustness across corrections
# =============================================================================
def figure_08() -> None:
    surv = pd.read_csv(SURV)
    labels_map = {
        "aaa_BB%_zseason":   "AAA BB%",
        "aaa_K%_zseason":    "AAA K%",
        "aaa_ISO_zseason":   "AAA ISO",
        "aaa_BABIP_zseason": "AAA BABIP",
        "age_at_debut":      "Age at debut",
    }
    surv["label"] = surv["predictor"].map(labels_map)
    surv = surv.dropna(subset=["label"])
    order = ["AAA ISO", "AAA K%", "AAA BB%", "Age at debut", "AAA BABIP"]
    surv = surv.set_index("label").loc[order].reset_index()

    fig, ax = plt.subplots(figsize=(11, 6.5))
    xs = np.arange(len(surv))
    w = 0.35
    ax.bar(xs - w/2, surv["OLS_coef"], w, label="Unweighted OLS",
           color="#6699CC", edgecolor="black", linewidth=0.8, alpha=0.9)
    ax.bar(xs + w/2, surv["IPW_coef"], w, label="IPW-corrected",
           color="#EE8866", edgecolor="black", linewidth=0.8, alpha=0.9)

    # value labels
    for i, (o, ip) in enumerate(zip(surv["OLS_coef"], surv["IPW_coef"])):
        ax.text(i - w/2, o + (0.15 if o > 0 else -0.15), f"{o:+.2f}",
                ha="center", va="bottom" if o > 0 else "top",
                fontsize=10, fontweight="bold")
        ax.text(i + w/2, ip + (0.15 if ip > 0 else -0.15), f"{ip:+.2f}",
                ha="center", va="bottom" if ip > 0 else "top",
                fontsize=10, fontweight="bold")

    ax.set_xticks(xs)
    ax.set_xticklabels(surv["label"])
    ax.axhline(0, color="black", lw=0.8)
    ax.set_ylabel("Standardized coefficient on MLB wRC+")
    ax.legend(loc="upper right", framealpha=0.9)
    add_title_and_subtitle(
        fig,
        "Skill-model coefficients: unweighted OLS vs. IPW-corrected",
        "Bars show standardized coefficients under the unweighted specification and "
        "after inverse probability weighting for cohort selection.",
    )
    fig.subplots_adjust(top=0.83)
    fig.savefig(FIGS / "08_coefficient_robustness.png")
    plt.close(fig)
    print("wrote figures/08_coefficient_robustness.png")


