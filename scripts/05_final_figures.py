"""
Regenerate publication-quality figures for the final paper.

Every figure is designed to be self-explanatory: title states the finding
(not the method), values are annotated on bars, colors carry meaning
(green = positive predictor, red = negative, gray = null), and captions
in the plot titles guide interpretation.

Figures produced (numbering matches Paper_Draft.md appendix):
  02  AAA wRC+ vs MLB wRC+ scatter — the motivation
  03  Skill coefficients — THE HEADLINE
  05  Model comparison — head-to-head predictive accuracy
  08  Coefficient robustness across survivorship corrections
  09  Before/after specification correction — the methodological story (NEW)

Other diagnostic figures (01 heatmap, 04 residuals, 06 pred-vs-actual, 07
selection) are left untouched — they're generated by the analysis scripts
and are already fine for a supplementary appendix.
"""

