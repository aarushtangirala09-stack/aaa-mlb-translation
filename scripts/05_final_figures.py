# FIGURE 05 — Model comparison (head-to-head)
# =============================================================================
def figure_05() -> None:
    # Values from the paired t-test output. Star convention matches Table 2:
    #   *** p < 0.001,  ** p < 0.01,  * p < 0.05,  n.s. otherwise.
    # p = 0.005 falls in the ** band (< 0.01 but not < 0.001).
    models = [
        ("Baseline\nraw AAA wRC+",           0.0614, 0.0035, None,   None,   None,   COLOR_BASE),
        ("Translation Score\n(5 skills)",     0.0599, 0.0063, -0.001, 0.52,  "TIE",   COLOR_NULL),
        ("Purified Score\n(no BABIP)",        0.0648, 0.0070, +0.003, 0.15,  "n.s.",  COLOR_NULL),
        ("Combined\nScore + wRC+",            0.0687, 0.0060, +0.007, 0.005, "**",    COLOR_WIN),
    ]

    fig, ax = plt.subplots(figsize=(11, 6.5))
    xs = np.arange(len(models))
    r2s = [m[1] for m in models]
    sds = [m[2] for m in models]
    colors = [m[6] for m in models]
    bars = ax.bar(xs, r2s, yerr=sds, color=colors, edgecolor="black",
                  linewidth=1, alpha=0.9, error_kw={"lw": 1.3, "capsize": 5})

    # value labels
    for i, (bar, m) in enumerate(zip(bars, models)):
        r2, _, delta, pval, verdict, _ = m[1:]
        ax.text(i, r2 + m[2] + 0.0025, f"{r2:.3f}", ha="center",
                fontweight="bold", fontsize=12)
        if delta is not None:
            sign = "+" if delta > 0 else ""
            is_win = verdict in ("**", "***")
            ax.text(i, -0.008, f"Δ = {sign}{delta:.3f}\np = {pval:.3f}   {verdict}",
                    ha="center", va="top", fontsize=10.5,
                    color=("darkgreen" if is_win else "#555"),
                    fontweight="bold" if is_win else "normal")

    ax.set_xticks(xs)
    ax.set_xticklabels([m[0] for m in models])
    ax.set_ylabel("Cross-validated R²  (higher = better)")
    ax.set_ylim(-0.025, 0.09)
    ax.axhline(models[0][1], color="gray", ls="--", lw=1, alpha=0.5, zorder=0)
    ax.text(3.35, models[0][1], " baseline", va="center", fontsize=9.5, color="gray")

    from matplotlib.patches import Patch
    legend_elems = [
        Patch(facecolor=COLOR_BASE, edgecolor="black", label="Baseline (raw wRC+)"),
        Patch(facecolor=COLOR_NULL, edgecolor="black", label="Not significantly better than baseline"),
        Patch(facecolor=COLOR_WIN,  edgecolor="black", label="Formally significant win"),
    ]
    # Place legend BELOW the plot area (horizontal) so it can never collide
    # with bar-value labels regardless of tallest bar.
    ax.legend(handles=legend_elems,
              loc="upper center", bbox_to_anchor=(0.5, -0.24),
              ncol=3, framealpha=0.9, frameon=True, fontsize=10.5)
    add_title_and_subtitle(
        fig,
        "Cross-validated projection accuracy across four models",
        "Head-to-head paired comparison on 10 CV shuffles. "
        "Δ = mean paired R² difference vs. baseline; p from two-tailed paired t-test.",
    )
    fig.subplots_adjust(top=0.83, bottom=0.28)
    fig.savefig(FIGS / "05_model_comparison.png")
    plt.close(fig)
    print("wrote figures/05_model_comparison.png")


